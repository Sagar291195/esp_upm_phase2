void setup_scr_logo(lv_ui *ui)
{
    //Main object of screen logo
    ui->screen_logo = lv_obj_create(NULL);

    static lv_style_t style_logo_screen_default;
	lv_style_reset(&style_logo_screen_default);

	lv_style_set_bg_color(&style_logo_screen_default, ESPUPM_BACKGROUND_COLOR);
	lv_style_set_bg_opa(&style_logo_screen_default, LV_OPA_COVER);
	lv_obj_add_style(ui->screen_logo, &style_logo_screen_default, LV_PART_MAIN|LV_STATE_DEFAULT);

	//Navier logo
	ui->logo_mainimage = lv_img_create(ui->screen_logo);
    lv_img_set_src(ui->logo_mainimage, &logo);
    lv_img_set_zoom(ui->logo_mainimage, 220);
    lv_obj_align(ui->logo_mainimage, LV_ALIGN_TOP_LEFT, 10, 55);

    //customer image
    ui->logo_customerimage = lv_img_create(ui->screen_logo);
    lv_img_set_src(ui->logo_customerimage, &logo_custom);
    lv_obj_align(ui->logo_customerimage, LV_ALIGN_TOP_LEFT, 60, 273);

    /******Events Initialization******/
    //events_init_logo(ui);

}



